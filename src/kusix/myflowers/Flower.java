package kusix.myflowers;

public class Flower {
	
	private String name;
	private String type;
	private boolean enabe;
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public boolean isEnabe() {
		return enabe;
	}
	public void setEnabe(boolean enabe) {
		this.enabe = enabe;
	}

}
